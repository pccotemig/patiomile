package br.com.patiomile.modelo;

public class TipoPessoa {

	private int tipoPessoaID;
	private String nomeTipoPessoa;

	public int getTipoPessoaID() {
		return tipoPessoaID;
	}

	public void setTipoPessoaID(int tipoPessoaID) {
		this.tipoPessoaID = tipoPessoaID;
	}

	public String getNomeTipoPessoa() {
		return nomeTipoPessoa;
	}

	public void setNomeTipoPessoa(String nomeTipoPessoa) {
		this.nomeTipoPessoa = nomeTipoPessoa;
	}

}
