package br.com.patiomile.modelo;

public class CategoriaCnh {

	private int categoriaCnhID;
	private String nomeCategoriaCnh;

	public int getCategoriaCnhID() {
		return categoriaCnhID;
	}

	public void setCategoriaCnhID(int categoriaCnhID) {
		this.categoriaCnhID = categoriaCnhID;
	}

	public String getNomeCategoriaCnh() {
		return nomeCategoriaCnh;
	}

	public void setNomeCategoriaCnh(String nomeCategoriaCnh) {
		this.nomeCategoriaCnh = nomeCategoriaCnh;
	}

}
